  nsWeakPtr mWindow;

  
  
  
  
  nsIWidget* GetWidget(nsPoint* aOffset = nullptr);
  nsIWidget* GetWidgetForElement(mozilla::dom::Element* aElement,
                                 nsPoint* aOffset = nullptr);

  nsIDocShell* GetDocShell();
