const CSSTranslate& CSSTransformComponent::GetAsCSSTranslate() const {
  MOZ_DIAGNOSTIC_ASSERT(mTransformComponentType ==
                        TransformComponentType::Translate);

  return *static_cast<const CSSTranslate*>(this);
}

