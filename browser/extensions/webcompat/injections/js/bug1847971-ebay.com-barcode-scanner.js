



"use strict";










if (!window.$feprelist_C) {
  const ENABLED_MESSAGE =
    "The barcode scanner feature has been force-enabled. See https://bugzilla.mozilla.org/show_bug.cgi?id=1847971 for details.";

  let value = undefined;

  function check(obj) {
    if (obj === null) {
      return false;
    }
    if (Array.isArray(obj)) {
      for (const v of obj) {
        if (check(v)) {
          return true;
        }
      }
      return false;
    }
    if (typeof obj === "object") {
      if ("barcodeEnabled" in obj) {
        obj.barcodeEnabled = true;
        return true;
      }
      for (const v of Object.values(obj)) {
        if (check(v)) {
          return true;
        }
      }
    }
    return false;
  }

  Object.defineProperty(window, "$feprelist_C", {
    configurable: true,

    get() {
      return value;
    },

    set(v) {
      try {
        if (check(v)) {
          console.info(ENABLED_MESSAGE);
        }
      } catch (_) {}
      value = v;
    },
  });
}
