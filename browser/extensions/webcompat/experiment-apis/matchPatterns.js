



"use strict";



this.matchPatterns = class extends ExtensionAPI {
  getAPI(context) {
    return {
      matchPatterns: {
        getMatcher(patterns) {
          const set = new MatchPatternSet(patterns);
          return Cu.cloneInto(
            {
              patterns,
              matches: url => {
                return set.matches(url);
              },
            },
            context.cloneScope,
            {
              cloneFunctions: true,
            }
          );
        },
      },
    };
  }
};
