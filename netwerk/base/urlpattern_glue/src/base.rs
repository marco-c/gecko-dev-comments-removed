




use nsstring::nsCString;
use std::ffi::c_void;









#[derive(Debug, Clone)]
#[repr(C)]
pub struct MaybeString {
    pub string: nsCString,
    pub valid: bool,
}

impl MaybeString {
    pub fn new(s: &nsCString) -> Self {
        Self {
            string: s.clone(),
            valid: true,
        }
    }
    pub fn none() -> Self {
        Self {
            string: nsCString::new(),
            valid: false,
        }
    }
}




