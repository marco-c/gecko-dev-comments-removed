





import puppeteer from 'puppeteer';
(async () => {
  const browser = await puppeteer.launch({
    protocol: 'cdp',
  });
  const page = await browser.newPage();
  await page.goto('data:text/html,<!DOCTYPE html><h1>example</h1>');
  await page.$('h1');
  await page.screenshot({path: 'example.png'});
  await browser.close();
})();
