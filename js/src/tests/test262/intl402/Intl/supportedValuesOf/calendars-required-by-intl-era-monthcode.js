





























const requiredCalendars = [
  "buddhist",
  "chinese",
  "coptic",
  "dangi",
  "ethioaa",
  "ethiopic",
  
  "gregory",
  "hebrew",
  "indian",
  "islamic-civil",
  "islamic-tbla",
  "islamic-umalqura",
  
  "iso8601",
  "japanese",
  "persian",
  "roc"
];

assert.compareArray(
  requiredCalendars,
  requiredCalendars.slice(0).sort(),
  "requiredCalendars is sorted"
);

const supportedCalendars = Intl.supportedValuesOf("calendar");

assert.compareArray(
  supportedCalendars,
  requiredCalendars,
  "only the required calendars are supported"
);

reportCompare(0, 0);
