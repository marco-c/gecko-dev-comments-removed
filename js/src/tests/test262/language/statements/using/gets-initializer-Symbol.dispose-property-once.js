







































































var resource = {
    disposeReadCount: 0,
    get [Symbol.dispose]() {
        this.disposeReadCount++;
        return function() { };
    }
};

{
  using _ = resource;
}

assert.sameValue(resource.disposeReadCount, 1, 'Expected [Symbol.dispose] to have been read only once');

reportCompare(0, 0);
