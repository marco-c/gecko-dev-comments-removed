









































































assert.throws(TypeError, function() {
  using x = { [Symbol.dispose]: true };
}, 'true');

assert.throws(TypeError, function() {
  using x = { [Symbol.dispose]: false };
}, 'false');

assert.throws(TypeError, function() {
  using x = { [Symbol.dispose]: 1 };
}, 'number');

assert.throws(TypeError, function() {
  using x = { [Symbol.dispose]: 'object' };
}, 'string');

var s = Symbol();
assert.throws(TypeError, function() {
  using x = { [Symbol.dispose]: s };
}, 'symbol');

